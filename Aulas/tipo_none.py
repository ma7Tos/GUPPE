"""
Tipo None

O tipo de dado None em Python representa otipo sem tipo, ou poderia ser conhecido também como tipo vazio, porém, falar
que é um tipo sem tipo é mais apropriado.

OBS.: O tipo None é SEMPRE especificado com a primeira letra maiuscula;

Certo: None
Errado: none

Quando utilizamos?

- Podemos utilizar None quando queremos criar uma variável e inicilizá-la com um tipo sem tipo, antes de recebermos um
valor final.

OBS.: O tipo None em Python é sempre considerado como False.
"""

numeros = None

print(numeros)
print(type(numeros))

numeros = 1.44, 1.34, 5.67

print(numeros)
print(type(numeros))